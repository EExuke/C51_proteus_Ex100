//-----------------------------------------------------------------
//  名称: 闪烁的LED
//-----------------------------------------------------------------
//  说明: LED按设定的时间间隔闪烁
//
//-----------------------------------------------------------------
#include <reg51.h>
#define INT8U	unsigned char
#define INT16U	unsigned int
sbit LED = P2^0;	//LED连接在P2.0引脚
//-----------------------------------------------------------------
// 延时函数
//-----------------------------------------------------------------
void delay_ms(INT16U x)          //1ms延时；
{
    INT8U t;
    while(x--)
        for(t=0;t<120;t++);    
}
//-----------------------------------------------------------------
// 主程序
//-----------------------------------------------------------------
void main()
{
    while(1)
    {
        LED = ~LED;             //取反闪烁；
        delay_ms(150);
    }
}